                  <p>We continue the rise to a pass, at an altitude of 3950 meters, before coming down to the ruins of Sayacmarca (3600 m), a small city composed of a maze of narrow streets, plots, houses, terraces, fountains, etc.<br>
                  The path continues quietly amount, and we go by an Inca tunnel and then walk throughout a forest, before reaching the last pass, at an altitude of 3700 meters.<br>
                  Below, the ruin of Phuyupatamarca: "City above the clouds", Inca relay, impresses by its circular walls and his ingenious system of aqueducts that still supplies the baths.</p>
                  <p>We enjoy a superb view on the snowcapped peaks and the Urubamba Valley. The descent of the other side is done under a thick canopy of bamboo.<br>
                  On the merits, appear the extraordinary ruins of Wiñay Wayna (2650m) on a rocky spur at the heart of the forest and overlooking t he Urubamba Valley. Dominated by a vast array of Inca terraces, the urban complex is separated in two by a grand staircase leading to a ceremonial area.<br>
                  This is where we will spend our last night in tents.</p>
