                 <p>We will leave Aguas Calientes by train. While waiting for the train, you will have the time to have lunch in one of the restaurants of the city. In addition, you can enjoy the hot springs in Aguas Calientes to relax before your departure.</p>

                <ul>
                  <li><b>Visiting time:</b> 2h</li>
                  <li><b>Transport time:</b> 4h (Aguas Calientes - Cusco) and 30 minutes (Bus Machu Picchu)</li>
                  <li><b>Altitude difference:</b> Aguas Calientes (2005 m) - Machu Picchu (2400 m) - Cusco (3400 m)</li>
                </ul>


