                <h3>Day 3: Chaullay - Lucmabamba - Archaeological Site of Llactapata - Hydroelectrica - Aguas Calientes</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>We will get up early in the morning to take the shuttle that took us to Lumacbamba. We will start the day hiking on the remains of the Inca Trail to reach the archaeological site of Llactapata (2650m), privileged place to discover the richness of the Inca culture and local flora. In addition, you will enjoy an extraordinary view of the city of Machu Picchu and the canyon of Vilcanota if the weather allows us.</p>

                 <p>You will begin to learn more about the Inca pilgrimage and the remaining vestiges to discover in Llactapata. We will descend, along the river, then to arrive at the Hydro Station (1950m), from where we will take the train until our stage of the day, the village of Aguas Calientes (2050m), located at the feet of Machu Picchu .</p>
                <ul>
                  <li><b>Hiking time:</b> 6 hours</li>
                  <li><b>Transport time:</b> 1h30</li>
                  <li><b>Climate:</b> Hot, insect presence</li>
                  <li><b>Elevation gain:</b> Chaullay (2950 m) - Lucmabamba (2000 m) - Llactapata (2600 m) - Hydroelectrica (1950 m) - Aguas Calientes (2005m)</li>
                </ul>

                <h3>Day 4: Aguas Calientes - Machu Picchu - Cusco</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
