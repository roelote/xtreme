                <h3>1st Day: Cusco – Cachora – Chiquisca </h3>
  <!--               <img src="../img/tours/inca-trail-depart point.jpg" alt="porters" class="img-thumbnail float-left mx-2" width="250"> -->
                <p>We will leave Cusco early in the morning at about 5:30 am. First we will head for the little village Cachora (2850m/9350f), a nice place surrounded by beautiful mountains and the majestic glacier Padreyoc (5571m/18278f). Here, we will also meet our horsemen who will accompany us during the trip. After having passed the overlooking Capuliyoc (2800m/9186f), we will continue descending to our campsite called Chiquisca (1930m/6332f).</p>
 
                <ul class="mt-2">
                  <li><b>Climate:</b> warm</li>
                  <li><b>Hiking time:</b> Approximately 6 hours</li>
