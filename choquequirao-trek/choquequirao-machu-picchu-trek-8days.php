                <h3>2nd Day: Chiquisca – Playa Rosalina – Marampata – Choquequirao </h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>After breakfast, we will first descend to La Playa Rosalina (1550m/4921f) and then climb up to the village of Marampata (2850m/9350f). From here, we will have a nice view of the archeological complex of Choquequirao (3033m/9951f). If we are lucky, we will even see a condor flying at nightfall. Our camping site in Choquequirao is about 25 minutes from the ruins at 2950m/9678f.</p>
                <ul>
                  <li><b>Climate:</b> both warm and cold</li>
                  <li><b>Hiking time:</b> 7 hours</li>
                </ul>

                <h3>3rd Day: Choquequirao – Choquequirao pass – Río Blanco </h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>At dawn, we will first head for the citadel of Choquequirao (3033m/9950) called “Cuna de Oro” - the ‘Cradle of Gold’ - and enjoy the quietness at this place. Beyond, we will explore this archeological site, of which only 30 % is restored, in more detail: we will visit the section of the palaces, the liturgic fountains, the temple of the high priest, the section of the llamas and the Ushnu. Thereafter, our trek continues towards the pass of Choquequirao (3270m/10728f) and passes the archeological site of Pinchaunuyoc (2450m/8038f). Finally, we will head down to the deepest point of the small canyon known as Río Blanco (1900m/6234f) or Río Silvestre where our camping site is situated.</p>

                <ul>
                  <li>Climate: warm with mosquitoes</li>
                  <li>Hiking time: 6 hours</li>

                </ul>

                  <h3>4th Day: Río Blanco – Maizal– San Juan pass - Yanama </h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>Due to the ecosystem’s constant changes, this day is both the most ardous and the most spectacular one. In Maizal (3000m/9843f) we will have a short break to catch some breath and to enjoy the view of the beautiful landscape. Since the Inca trail in this region is not in good conditions during the rainy season, we ask you to watch your steps carefully. In San Juan (4170m/13681f) we will enjoy the beautiful view of the valleys, the mountains and the glaciers, from which some of the latter peak more than 5000m/16404f. Our camping place will be in the little village of Yanama (3530m/11581f).</p>
                <ul>
                  <li>Climate: warm and cold with mosquitoes</li>
                  <li>Hiking time: 5 hours </li>
                </ul>

                <h3>5th Day: Yanama – Yanama pass – Totora pampa</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>Early in the morning, we will head for the highest pass of this trip - the Yanama (4670m). If we are lucky we will even see a condor flying. During the day, we will enjoy frequently the view of snow capped mountains such as Salkantay (6264m) and Pintuyu (5450m). Our camping place will be in the valley of Totora pampa (3200m).</p>
                <ul>
                  <li>Climate: warm with mosquitoes.</li>
                  <li>Hiking time: 8 hours</li>
                </ul>
                <p><b>Note:</b> According to the group’s whishes, it is possible to change the itinerary a bit and visit the hot springs in Santa Teresa (1830m/6004f) this day.</p>

                <h3>6th Day: Totora pampa – La Playa</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>In Totora pampa we will get the chance to meet Quechua-speaking Andean people who live from agriculture and cattle breeding. The vegetation on our way gets more and more bizarre and reveals its diversity of orchids, bromelains and other plants. We will also see some plantations for coffee, papaya, passion fruit and avocado. Our camping site will either be in La Playa (2100m/6890f) or in Lucmabamba (2020m/6627f).</p>
                <ul>
                  <li>Climate: warm whit mosquitoes</li>
                  <li>Hiking time: 7 hours</li>
                </ul>

                <h3>7th Day: La Playa – Llactapata – Hidroeléctrica – Aguas Calientes </h3>
                <p>Our journey starts early in the morning. First, we will follow the Inca Trail till Llactapata (2650m/8694f). This archeologic site is situated amidst a bizarre vegetation and offers an exceptional view of the mountain Machu Picchu. Our trek continues towards the hydroelectric station (1950m/6398f) from where we will take the train to Aguas Calientes (2050m/6726f). In Aguas Calientes we will spend the night in a hotel.</p>
                <ul>
                  <li>Climate: warm</li>
                  <li>Hiking time: 6 hours</li>
                </ul>
                <h3>8th Day: Aguas Calientes – Machu Picchu – Cusco</h3>
                <p>We will leave our hotel after breakfast at 5:30 am and head for Machu Picchu (2400m/7874f) by tourist bus. We will arrive at the citadel around 6:00 am. After a guided tour around the archeological site, including information about life and culture of the Incas, you will have enough time to enjoy Machu Picchu and Wayna Picchu (2750m/9022f) on your own. We will leave Aguas Calientes by train either at 2:55 pm or at 6:10 pm. Until then, you will have time to have lunch in one of the nice restaurants. Moreover, you can visit the hot springs in Aguas Calientes to relax a bit.</p>
                <ul>
                  <li><b>Climate:</b> moderate warm</li>
                  <li><b>Hiking time:</b> 02 hours</li>
                </ul>

