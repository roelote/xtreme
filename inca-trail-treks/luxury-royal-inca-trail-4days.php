                  <li><b>Distance traveled:</b> approximately 9 km</li>
                  <li><b>Altitude difference:</b> Chaquicocha (3650 m) - Phuyupatamarca (3600 m) - Wiñayhuayna (2700 m)</li>
                </ul>

                <h3>Day 4: Wiñayhuayna - Machu Picchu - Cusco</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>Departure very early this day gently with the flashlight, to the Gate of the Sun (Inti Punku) located at 2700 meters above sea level where you will enjoy your breakfast. From here you will have a panoramic view of Machu Picchu and Wayna Picchu. We will end this unforgettable hike by taking advantage of the sunrise on the sacred site of Machu Picchu that will make us understand all the meaning and strength of worship that the Incas gave to the Sun God.</p>

                 <p>It is a magical moment that can only be appreciated after the pilgrimage we have just made in the footsteps of the Incas. After this magical moment, we will descend to the actual site of Machu Picchu (2400 meters altitude), where our guide will make us visit and we will see how was life in the citadel, and the cults and ceremonies that realized the Incas in the many temples and sacred places present on the site. After a 2-hour guided tour, you can enjoy the magic of Machu Picchu sanctuary at your own pace. We will join Aguas Calientes in the afternoon where you can relax peacefully in the baths of natural hot water and wash the effort of these four days in the showers offered by the baths. Then we will take the train back to Cusco.</p>
               

