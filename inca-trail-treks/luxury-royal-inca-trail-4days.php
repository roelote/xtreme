                <h3>Day 2: Yuncachimpa - Llulluchapampa - Warmihuañusca Pass - Pacaymayu - Runkurakay Pass - Chaquicocha</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>The second day is a day with high altitude, it means that everyone will have to climb at his own pace, of course this day will consist of many breaks. Early in the morning we will walk through the Llulluchapampa Valley (3850m). The first 3 kilometers, you will appreciate the wooded and humid region, rocked by the sound of the water. The trail will climb slightly through beautiful subtropical vegetation. The slope will then become steeper and the forests will gradually give way to bare landscapes of the "puna" altitude. We will climb painfully to the Warmiwañusca Pass (4200 meters, the "Pass of the Dead Woman" in Quechua). </p>

                <p>This climb is known as the most grueling of all the Inca Trail. After a short break, we will descend to the other side, up to the Pacaymayo River Valley (3500 meters), before starting to climb again to the archaeological site of Runkurakay (3950 meters). Perched on a rocky outcrop, this site served as a watchtower and tambo (relay) to the Chasquis (the Inca messengers). We continue the climb to the second pass, 3950 meters above sea level, before descending to the ruin of Sayacmarca (3600 meters), a small city composed of a labyrinth of alleyways, squares, houses, terraces, fountains, etc. . We will quickly reach the site of our second camp, in Chaquicocha, 3650 meters above sea level and we will enjoy an impressive view of the mountains. After an excellent dinner prepared by our cooks, it will be time for a well deserved rest!</p>
                <ul>
                  <li><b>Difficulty level:</b> difficult</li>
                  <li><b>Hiking time:</b> 8 hours</li>
                  <li><b>Distance traveled:</b> approximately 15 km</li>
