                <h3>Day 1: Cusco - Km 82 - Wayllabamba - Yuncachimpa</h3>
  <!--               <img src="../img/tours/inca-trail-depart point.jpg" alt="porters" class="img-thumbnail float-left mx-2" width="250"> -->
                <p>We will leave Cusco by private bus at 5.30am. Before reaching our starting point km 82, we will make a stop at Ollantaytambo for breakfast. Namely, km 82 is the name for the official starting point of the Inca Trail. We will follow the Sacred Valley of the Incas to this point, where we will meet the rest of the team (porters, cooks ...). The trek will start with a suspension bridge that crosses the Urubamba River at an altitude of 2600 meters. The first day of the trek, the walk will be rather easy and will be a good warm-up for the following days. After a short walk, we will discover the first archaeological site of the trek: Llactapata, agricultural center with large terraces Incas. We will take this opportunity to have our first meal, before continuing our walk. We will go up the river Cusichaca to the village of Wayllabamba (3000 meters of altitude), then we will join Yuncachimpa (3300 meters), where we will spend our first night in camp. From this place, we will have the opportunity to enjoy a magnificent view of the mysterious Nevado de Verónica.</p>
 
