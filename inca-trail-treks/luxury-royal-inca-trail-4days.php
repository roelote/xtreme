                </ul>

                <h3>Day 3: Chaquicocha - Phuyupatamarca - Wiñayhuayna</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>This third day will be the most beautiful of the trek. You will notice a change in the ecosystem throughout your walk in the rainforest of Wiñaywayna. We will also walk along two lovely little lakes (3900m). We will start the day by going up slowly, and we will cross an Inca tunnel of about twenty meters then we will cross a forest, before reaching the last pass, at 3700 meters altitude. Below, the ruins of Phuyupatamarca: the "City above the clouds" (3600m), relay Inca, impresses by its circular walls and its ingenious system of aqueducts which still feeds the baths. We will enjoy a superb view of the snow-capped peaks and the Urubamba Valley.</p>
                 <p>The descent on the other side is under a thick bamboo cover. In the background, will appear the extraordinary ruins of Wiñay Wayna "Eternally young" (2700m) on a rocky spur in the heart of the forest and overlooking the valley of the Urubamba. Dominated by a vast set of Inca terraces, the urban complex is separated in two by a grand staircase that leads to a ceremonial area. The stairs are zig-zagged until we reach a red building, our last camping place where we can have access to hot water to fill our water bottles.</p>

                <ul>
                  <li><b>Level of difficulty:</b> medium</li>
