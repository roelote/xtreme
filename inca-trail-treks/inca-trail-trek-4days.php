                    <li class="my-2"><b>(4) Camping sites:</b> the camping sites on the Inca Trail can be different from the ones described in our itinerary (Yuncachimpa, Chaquicocha and Wiñaywayna). This is due to a limitation of the places (Wiñaywayna is almost always fully booked). The camping sites (especially Wiñaywayna) are well known with the public. Therefore they will book their places well in advance (certainly when you want to go in the high season from May to September you have to book months in advance). If there isn’t any place anymore, the Ministry of Culture (entity of the government which supervises and controls the availability of the quotas for entering the Inca Trail) will assign other camping sites (Wayllabamba, Pacaymayu and Phuyupatamarca).</li>
