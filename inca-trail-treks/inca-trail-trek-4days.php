               <h2 class="my-3">Inca Trail 4 Days, Trek Itinerary:</h2>
                <h3>1st Day: Cusco - Km 82 – Wayllabamba - Yuncachimpa</h3>
                <img src="../img/tours/inca-trail-depart point.jpg" alt="porters" class="img-thumbnail float-left mx-2" width="250">
                <p>We will leave Cusco (3350m/10990f) at 5:30 am by a tourist bus heading for the Sacred Valley of the Incas. Before we reach our starting point of walk, km 82, we will make a short break in Ollantaytambo for having the option to get breakfast. The official beginning of the Inka Trail is called km 82. Here we will meet the rest of the group and take care of the registration facilities. After crossing the Rio Urubamba (2200m) we will start the legendary Inca Trail. The first day the trail is easy to walk and a good warm up for the following days. In Miskay we’ll have lunch at a small river bank while getting some information about the archeological complex of Llactapata. After that, we will continue our hike to Wayllabamba (3100m/10170f) and ascend to Yuncachimpa (3300m/10826f), the camping site we will spend the night in. From here, we have a great view of the mysterious Nevado de Verónica.</p>
                <div class="clearfix"></div>
                <ul class="mt-2">
                  <li><b>Level of difficulty:</b> moderate</li>
                  <li><b>Hiking time:</b> 6-7 hours</li>
                  <li><b>Total distance:</b> 11km approx.</li>
                </ul>

                <h3>2nd Day: Yuncachimpa - Llulluchapampa – Warmihuañusca pass - Pacaymayu - Runkurakay pass - Chaquicocha</h3>
                <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300">
                <p>The second day is the ‘Free-Hike-Day’; that means, that everybody should walk in his/her own pace – of course you will meet during the small breaks or see each other while beating the stairs. Early in the morning the trail first leads through the Valley of Llulluchapampa (3850m/12631f). The first 3km are step - enjoy the humid woodland and the water sounds. From Llulluchapampa follow the way for 2-3 hours until you reach the highest point of this section, the Warmihuañusca Pass (Dead Woman Pass) (4200m/113780f). This climb is known as the most difficult part of the Inca Trail. After a short break at the summit, you continue your hike down with a long and steep descent to the river Pacaymayo (Sunrise River) towards the Runkurakay pass (3050m/10007f) until you reach our camping site called Chaquicocha at (3650m/11975f). Here you can relax your body from the tough day while enjoying an amazing view over the mountains.</p>
                <ul>
                  <li>Level of difficulty: challenge</li>
                  <li>Hiking time: 8-9 hours</li>
                  <li>Total distance: 15 km approx.</li>
                </ul>

                  <h3>3rd Day: Chaquicocha - Phuyupatamarca – Wiñayhuayna</h3>
                <img src="../img/tours/winaywayna.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="320">
                <p>The third day is the most beautiful one. You can notice a change in the eco-system while walking to the tropical forest of Wiñaywayna. We will pass two small lakes at the top of the second pass (3950m). It’s a gentle climb through beautiful cloud forest and a small Inca tunnel before the third pass (3700m) offers a grand view over the Rio Urubamba Valley. The nearest settlement is named Phuyupatamarca/"Town above the clouds" (3600m/11811f). A well preserved site with long chain Inca baths where one can enjoy impressive ancient engineering. We pass another Inca Tunnel before visiting the ruins of Wiñaywayna/"Forever young" (2650m/8694f). The Inca stairs change into a zigzag trail until you reach a red-roofed white building, our last camping site, and also a location with hot water (S5) and bottled drinks.</p>
                <ul>
                  <li><b>Level of difficulty:</b> normal, unforgettable (easy)</li>
                  <li><b>Hiking time:</b> 5-6 hours</li>
                  <li><b>Total distance:</b> 10 km approx.</li>
                </ul>

                <h3>4th Day: Wiñayhuayna - Machu Picchu - Cusco</h3>
                <img src="../img/tours/machu-picchu-view.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="320">
                <p>After our breakfast at 4:30 am, we will head - equipped with our flashlights - to the Inti Punku (Sun Gate) to get the first glimpse of Machu Picchu and watch the sunrise over Machu Picchu and Wayna Picchu. The last triumphal descent will take you almost one hour before we visit the citadel of Machu Picchu (2400m//7874f) around 8.00 am. You can enjoy its spiritual charm during a guided tour (about two hours) that helps you to understand the meaning and ancient use of the different buildings. After this tour you will have enough time to explore the archeological site on your own (f.ex. go to the Inca Bridge, explore the variety of buildings or climb Wayna Picchu for an amazing panorama picture). A bus takes you down to Aguas Calientes. Here you can soak away your aches and pain in the town’s hot springs (S10). We will leave Aguas Calientes in the afternoon.</p>
                <ul>
                  <li>Level of difficulty: normal, unique (easy)</li>
                  <li>Hiking time: 2-3 hours</li>
                  <li>Total distance: 7 km approx.</li>
                </ul>

