                    <li class="my-2"><b>(6) Your health:</b> It is a common misconception that because many people do the Inca Trail it must be easy. It is not. The Trail is 45km (26 miles) long and involves great physical exertion to complete. Combined with high altitude and extreme weather the trek can be hard work. Therefore we strongly recommend ensuring that you are well acclimatized, in good shape and healthy. This way you will enjoy your trip from the beginning on. A participant must declare if he/she has any sickness or medical disorder and must bring his/her own medicines.</li>
                    <li class="my-2"><b>(7) Weather:</b> The Andean weather can be unpredictable, despite the sharp divide between dry and wet seasons. The dry season runs from May to October or November; the rainy season goes from December through April. Nevertheless, Inca Trail lies in the forested.</li>
