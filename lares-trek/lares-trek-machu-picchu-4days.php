                <h4>The art of Andean textiles:</h4>
                  <p>The local tradition of weaving textiles in the Andes dates from ages ago. Numerous indigenous communities in the region, like the Huacawasi have preserved their knowledge and techniques of weaving textiles, as well as their ancient fashion. The weaving is done in a traditional way, using a weaving tool, consisting in 2 bars with a a weaving thread in between. The local people take the wool from the lama’s to make their traditional cloting with. The woolfirst is first spun and then colored with natural materials such as flowers. Every community is distinguished by its proper colors and drawings, representing the daily activities of the local communities.</p>

