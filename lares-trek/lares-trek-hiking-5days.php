                </p>
                <p>After this pass, we will go to the village of Patacancha (3750 m), where our bus will be waiting to take us to the Inca town of Ollantaytambo (2700 m). From there you will experience the incredible train experience to Aguas Calientes with an outstanding panoramic view. The night will be in a hotel, comfortable, in the city of Aguas Calientes.</p>

                <ul>
                  <li><b>Walking time:</b> 6 hours</li>
