                <h3>Day 2: Cancha Cancha - Pachacuteq Pass - Quishuarani</h3>
                  <p>After breakfast, we will start the climb to the Pachacuteq Pass (4458 m), the highest point of the trek. From up there you will be treated to a spectacular view of the snowy peaks, Pitusiray, Lac Pachacuteq and all around the Valley. After the Pass, we will continue walking down, this time, to the next valley to arrive at the village of Quishuarani (3700 m) where we will spend the night. Along the way, the landscapes will be breathtaking - with frozen lakes, waterfalls, and snow-capped peaks. This region is also rich in bird species and it is possible to see the Andean ibises, Andean geese and flamingos among others.</p>.

