                  <li>Mild climate</li>
                  <li><b>Altitude difference:</b> Huacahuasi (3750 m) - Ipsaycocha Pass (4350 m) - Patacancha (3750 m) - Ollantaytambo (2700 m) - Aguas Calientes (2005 m)</li>
                </ul>
                <h3>Day 5: Aguas Calientes - Machu Picchu - Cusco</h3>
