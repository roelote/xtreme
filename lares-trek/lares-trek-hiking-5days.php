                <p>We will continue to cross several lands and we will meet Andean local families, where agriculture and textile expertise inspired by Andean gods and symbols is very famous in this region. Once the Qasa Cruz Pass (4100 m) past we will go to the community of Huacahuasi (3800 m). With this community you will have the opportunity to share a unique and authentic moment by learning more about their culture, customs, traditions and their daily lives. We will set up the camp near the community where you can explore the surroundings if the desire and time allow you.</p>
                <ul>
