                <h3>Day 1: Cusco – Chinchero – Ollantaytambo – Malaga Pass – Santa Maria - Maranura (descent by mountain bike)</h3>
  <!--               <img src="../img/tours/inca-trail-depart point.jpg" alt="porters" class="img-thumbnail float-left mx-2" width="250"> -->
                <p>We will pick you up at your hotel in Cusco at about 6h30 with our private transport and we will take you further to the village of Chinchero. This typical village that still possesses a lot of Inca ruins, is one of the main places in the Sacred Valley of the Incas. From this village we will follow the Vilcanota river until we arrive in the village of Ollantaytambo. This village had an important fortress during the reign of Manco Capac, especially when the Spanish began to gain land in this region (1535). From Ollantaytambo, we will climb the pass of Abra Malaga, which is situated at a height of 4350 meters above sea level. </p>

                <p>We will arrive this pass at around midday. On this pass, you will be given mountain bikes which you can use to make the descent on asphalted roads in the midst of the jungle (when the weather doesn’t allow it, we will perform this part of the trek by car). We ride our mountain bikes until the little community of Carrizales where we will enjoy our breakfast and a little rest. After our well-deserved rest, we continue to descent with our mountain bikes through attractive villages in the midst of the tropical forest, which will take us roughly 3 or 4 hours. On the way we will visit the archaeological site of Wamanmarka. After this we will offer you the time to enjoy the beautiful landscaper with its typical vegetation and marvelous fauna. Arrived at Santa Maria (1430m) we will have lunch and pass the night in the house of a local family in Maranura (about 10minutes from Santa Maria). In the afternoon there will be some exchange with the family concerning the harvesting of their coffee and coca, depending on the season. If you wish, you can also do a quadbike tour, discovering the landscape full of cascades, fauna and flora (on supplement, ask your agency for the price).</p>


                <h3>Day 2: Maranura – Santa Teresa (Trekking)</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>We will wake up at about 6h00 to taste the delicious local coffee and fruit juices (according to the season) as well as the succulent breakfast made by our cook (7h30). We will walk along the bank of the Vilcanota river and where the landscape becomes a bit more steep begins one of the ancient inca trails.</p>

                 <p>Here there are also abysses until 800 meters deep. We take our breakfast in the village of Qellomayo, 1 hours of trek further away. We will be able to see the local fauna from very close, in particular the hummingbird, parrots, eagles etc. We also have the chance to pick to local fruits such as oranges, mandarins, pacay (Andean fruit), mangos, cacao, coffee, etc. We continue along the bank of the river to arrive at the hot tubs of Cocalmayo, close to the village of Santa Teresa. After having enjoyed this hot springs we continue to Santa Teresa (1850m). Here we will have diner and rest.</p>
               

                <h3>Day 3: Santa Teresa – Station Hydroélectrique – Aguas Calientes (Trekking)</h3>
<!--                 <img src="../img/tours/sayacmarca.jpg" alt="machu picchu" class="img-thumbnail float-left mx-2" width="300"> -->
                <p>In the morning we will have the opportunity the do zipline (on supplement, the reservation is to be done directly in your agency while booking the whole trek). This is a unique and an adventurous experience full of adrenaline passing by one side of the valley to the other at a cable. The ziplining begins at 7hours in the morning and takes until 10hours. At this time there will be a bus taking us to the Hidroelectrical station where we will have our breakfast.</p>

                 <p>We will continue our trek on the bank of the Urubamba river for about 3 hours until we arrive at Aguas Calientes, a little town at the feet of Machu Picchu. On our way to this town, we will visit the natural park of Mandor where we discover a variety of local flowers and when we are lucky, local bird life as well… In Aguas Calientes town we will have lunch and rest during the night. There will be also a chance to enjoy the hot tubs in Aguas Calientes (on supplement).</p>

                 <h3>Day 4: Machu Picchu – Cusco</h3>
                 <p>We will be leaving our hotel early in the morning after having had our breakfast and we will immediately take the bus to Machu Picchu at 5h30 which takes roughly half an hour. In the bus we will be amazed by the astonishing view of the sunset over Machu Picchu. Our guide will offer you a guided visit of about 2 hours and after this you will have time to visit Machu Picchu and maybe Wayna Picchu (optional, to be reserved in advance) at your own pace. When you have survived the though climb of this mountain you will be recompensed by the astonishing view over Machu Picchu.</p>
                 <p>Tourist busses will offer you the descent back to Aguas Calientes, from where we will take the train to Ollantaytambo, where our bus awaits you to continue to Cusco.</p>
                

